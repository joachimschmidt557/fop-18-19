package Math;

public interface StringMath {
	
	/**
	 * @param x
	 * @param y
	 * @return the result of the addition of x and y
	 */
	String add(String x, String y);
	
	/**
	 * @param x
	 * @param y
	 * @return the result of the subtraction of y from x
	 */
	String sub(String x, String y);
	
	/**
	 * @param x
	 * @param y
	 * @return the result of the multiplication between x and y
	 */
	String mul(String x, String y);
	
	/**
	 * @param x
	 * @param y
	 * @return the result of x divided by y
	 */
	String div(String x, String y);
	
}
